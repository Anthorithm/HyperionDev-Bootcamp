# sentence with value
sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog"

# Replace '!' with a blank space
sentence_without_exclamation = sentence.replace('!', ' ')

# Print the sentence with spaces
print(sentence_without_exclamation)

# Convert to uppercase
uppercase_sentence = sentence_without_exclamation.upper()

# Print the sentence in uppercase
print(uppercase_sentence)

# Print the sentence in reverse
reverse_sentence = sentence_without_exclamation[::-1]
print(reverse_sentence)


