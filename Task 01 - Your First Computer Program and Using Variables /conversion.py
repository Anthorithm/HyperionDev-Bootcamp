# Pseudocode:

# Declare variables
num1 = 99.23
num2 = 23
num3 = 150
string1 = "100"

# Convert variables
# 1. Convert num1 into an integer
int_num1 = int(num1)

# 2. Convert num2 into a float
float_num2 = float(num2)

# 3. Convert num3 into a string
str_num3 = str(num3)

# 4. Convert string1 into an integer
int_str1 = int(string1)

# Print out the variables on separate lines
print("Converted values:")
print(int_num1)
print(float_num2)
print(str_num3)
print(int_str1)
